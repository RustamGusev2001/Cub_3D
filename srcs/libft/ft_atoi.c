#include "libft.h"

int	ft_atoi(const char *str)
{
	long	n;
	long	k;

	n = 0;
	k = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str ++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			k = k * (-1);
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		n = n * 10 + *str - '0';
		if (n > 2147483647 && k == 1)
			return (-1);
		if (n > 2147483648 && k == -1)
			return (0);
		str ++;
	}
	return (n * k);
}
